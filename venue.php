<h1>BALI</h1>
<p align="justify">Bali lies between Java and Lombok in the tropical seas of the Indonesian archipelago.  The extraordinary physical beauty of the island -- volcanic mountains, terraced rice paddies, tropical beaches and lagoons, and forests -- draws many tourists to Bali. But it is the amazingly artistic culture and fascinating Hindu religious traditions of the Balinese people that brings tourists back to Bali time and time again. Stepping into a culture that lives and breathes artistic beauty astonishes the senses and refreshes the spirit. We considered those effects in selecting Bali as the conference site -- minds jolted awake and open by the exotic splendor of Bali and her people, we gather with our colleagues from the world over to see, hear, and share our work against vivax malaria and to recharge the vigor of that work with new knowledge and friendships.</p

