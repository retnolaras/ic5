        <td height="360" valign="top"><h1>EVENT</h1>
          <blockquote>
        <p>We are pleased to announce the organization of this fifth gathering of scientists committed to research on malaria caused by <em>Plasmodium vivax</em>. The momentum of research on this parasite has gained dramatically in recent years after long decades of almost complete neglect. Once held every four years, we now aim for a biannual event in order to keep up with the rapidly breaking advances in vivax malaria research.<br />

        <p>Over 2.5 billion people live at risk of infection by <em>P. vivax</em> and probably over 100 million suffer clinical attacks each year. It is the most widespread, complex, and difficult to investigate infection among the four other species of plasmodia that routinely cause human malaria. It is also the most difficult species to manage as a clinical and public health problem. Despite having been misperceived as relatively harmless in past decades, today <em>P. vivax</em> is acknowledged as very often being associated with pernicious and threatening illness in patients. This realization has underscored the necessity of greatly accelerating the progress of research on this parasite – which this conference precisely aims to accomplish.</p>
          
        <p>The conference is structured for biomedical researchers, and clinical and public health investigators – at all stages of career – engaging the problem of <em>P. vivax</em> in the laboratory, clinic, or field. We will bring plenary speakers representing the most exciting and important advances against <em>P. vivax</em> to present their findings, and to guide symposia/workshops in their fields of endeavor.  Attendees shall be immersed in not only in the culture of vivax malaria researchers and their work, but also that of the beautiful and exotic culture of the island of <a href="http://en.wikipedia.org/wiki/Bali" target="_blank">Bali</a> in the tropical seas of the <a href="http://en.wikipedia.org/wiki/Indonesia" target="_blank">Indonesian archipelago</a>. All are welcome and registration is open.          </p>

